# noobGuide
@ title 欢迎新人
@ contentSize 25
以下给出了前期的主要学习路线，其他技巧如定式和T-Spin等对实力帮助很小（此处不认为靠定式对战秒其他对手是通用的有效实力）
注意，三段应当同时进行，不是A→B→C
~~5
A.堆叠能力练习方法

A-1.手上的块可以放的时候先别急着放，看看下一块有没有地方，如果放不下去就看看手上的能不能换个地方
A-2.尝试把地形尽量控制得平整，因为大多数情况比较平的地形来啥块都比较容易放得下去
A-3.允许暂存的时候可以多想想手里和暂存的块和后续几块应该怎么安排顺序，使地形能持续平整
~~2
B.操作效率与速度练习方法

B-1.不要每一块都拿影子去对形状对位置，尽量自己脑中想象方块旋转后是什么方向，想好了再开始动手
B-2.学习双旋，能逆时针转一次就不要顺时针转三次
B-3.学习极简，刚开始先不管速度，保证正确率几乎100%，熟悉后再去提速
~~2
C.堆叠能力考核

C-1.允许暂存，稳定完成40行不死
C-2.禁用暂存，稳定完成40行不死
C-3.允许暂存，稳定全部消四完成40行
C-4.禁用暂存，稳定全部消四完成40行
~~2
以上均为经验总结的模糊方法与目标，故C的考核可以根据自身情况调整严格程度（如“稳定”的具体成功率）。
注：完成C部分的考核后，需要在未来一直注意没有上限的A1，这是方块的根本元素之一，有了强大的读next能力可以快速上手任何变种规则。

# keybinding
@ title 键位设置向导
键位设置原则参考：
1.不要让一个手指管两个可能同时按的键，通常只有几个旋转键不需要同时按，其他功能推荐都单独给一个手指；
2.除非已经锻炼过小拇指，最好不要用，一般食指和中指最灵活，自己觉得舒服为准；
3.没必要参考别人的键位设置，每个人都不一样，只要不违反前几条规则，就几乎不会对成绩产生影响。

# handling
@ title 手感相关向导
决定手感的几个主要因素：
1.输入延迟受设备配置或者设备状况影响，可以重启或换设备解决；
2.程序稳定性不好，时不时卡顿，可以尝试把设置画面效果拉低；
3.规则设计有意为之；
4.参数设置设置不当；
A.姿势不当，不便用力；
B.换新键位/新设备后不适应操作习惯；
C.肌肉疲劳反应和协调能力下降，需进行睡眠或体育运动。

