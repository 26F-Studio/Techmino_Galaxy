# piece
@ title 形状、颜色、朝向
在标准规则下：
所有的方块都是“四连块”，即四个正方形共用边连接成的形状。
由于通常不允许进行翻转动作，故四连块一共有七种，根据它们的形状一般分别叫做Z、S、J、L、T、O、I。
同种方块颜色是固定的，方便对形状进行更直观的记忆。
同种方块的生成朝向是固定的，同时部分旋转系统中踢墙表的选择也由方块从哪个朝向转到哪个朝向决定。
当我们需要描述方块的状态时，使用 0 R 2 L 四个字符来描述方块的朝向，其中0为生成时的朝向，R（Right）是从0朝向开始顺时针旋转90°后的，L（Left）是逆时针90°后的，2（任意方向旋转两次）是旋转180°后的。

