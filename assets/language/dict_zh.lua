All Clear, 消除场地内所有方格。
另有两个变种：
Half Clear：“下方有剩余方格”的全消
Color Clear：消除了非垃圾行的所有方格

# charge
@ title 充能（CHG / B2B）
多次特殊消除（消四或卡块消）之间没有间隔普通消行时可以获得充能，为后续的攻击提供加成。
CHG：Charge
B2B：Back to Back, 连续

# sequence
@ title 序列生成器
不同的规则会使用不同的序列生成算法。
~~
纯随机（Random）
独立随机生成每一个新的方块，有保证不连续出两个相同块的变种。
缺点是可能连续生成太多SZ（洪水）或者很久没有I（干旱）的情况。
~~
历史（History）
一类序列生成方式的统称，具体的类型例如His4 Roll6（h4r6）。
效果是在随机生成新的方块时，先在七个块中直接随机，然后对比前4个生成的方块，如果有相同的就重新随机，直到和最近四个都不同或者重试了6次，大大减少了洪水和干旱的情况。
~~
历史池（HisPool）
介于History和Bag7之间的一种生成方式。
~~
包循环（7-Bag）
从开局起，每七个块包含七种形状各一个，只随机打乱顺序。
这种序列生成方式彻底避免了很久不出某种块和某种块来得特别多的情况。

# gravity
@ title 重力（Gravity）
指在不操作的情况下方块自然下落的速度。
下落速度较慢时可以用“x秒一格”和“一秒x格”，很快的时候会改用G为单位，意思是每一帧向下移动多少格。帧一般以传统的60FPS计，也就是说1G为每秒下落60格，半秒之内就能触底。
特别地，方块的下落速度可以为无穷大，方块将不再能悬空或飞跃深坑。

# delays
@ title 动画延迟
锁定延迟（Lock Delay）
指在不操作的情况下，“方块自然下落至触地”到“方块锁定不能再移动”之间的时间。
一般规则下该延迟可以被操作刷新，故哪怕在无穷大重力时方块瞬间落地，依然有大量操作的空间。
~~
进场延迟（Entry Delay）
进场延迟，指“当前方块锁定”到“下一个方块出现”之间的时间。
别名ARE。
~~
消行延迟（Line Clear Delay）
指消行动画持续的时间。
~~
窒息延迟（Suffocating Delay）
方块出生后和场地有重叠时进入窒息状态持续的时间。时间结束后方块强制锁定，本局结束。

# death_condition
@ title 死亡判定
死亡判定通常包括下列的至少一项：
1. 窒息（Block Out），生成位置被阻挡
2. 超界（Lock Out），方块锁定时完全在场地之外
3. 超高（Top Out），场地总高度大于一定值

# invisible
@ title 隐形（Invis）
半隐：方块在锁定一段时间后（几秒钟左右）会变为隐形状态的规则。
全隐：方块在锁定后立刻开始进入隐形状态的规则。

# chain
@ title 连锁（Chain）
在特定规则下，消除可能会导致悬空的结构下落，此下落进一步导致的消除称为连锁消除。

# spike
@ title 爆发（Spike）
指短时间内打出大量的攻击，得名于其在攻击-时间的曲线图表上的视觉外观为尖刺（Spike的本义）。

# speeds
@ title 速度、攻击
表示按键速度的常用标准有KPS、KPM。
KPS：Key per Second, 按键每秒
KPM：Key per Minute, 按键每分
~~3
表示落块速度的常用标准有PPS、BPM、LPM。
PPS：Piece per Second, 块每秒
BPM：Block per Minute, 块每分，也有写作PPM的，意思相同
LPM：Line per Minute, 行每分，其中“行数”有两种计算方式，一是字面的消了几行，二是只用落下的方块数乘以4/10来换算，排除了垃圾行等因素影响
~~3
表示攻击强度的常用标准有APM、APL。
APM：Attack per Minute, 攻击每分
APL：Attack per Line, 攻击每行（行的定义有两种，详见LPM词条），也叫效率

# misaction
@ title 误操作
误操作通常有误放（Mis-drop）和误暂存（Mis-hold）。误放指因为操作问题无意中将方块锁定在了错误的地方，误暂存指本来不用暂存的时候按了暂存，导致需要的块进暂存拿不出来了（通常不允许连续暂存）。

# finesse
@ title 极简（Finesse）
用最少的按键数将方块移到想去的位置的技术，节约时间和减少误操作。
通常只考虑从空中直接落下并锁定的落点，不考虑软降后再操作的那些。
极简的前置技术包括：
~~
双旋：
指能够使用顺时针+逆时针两个旋转键的技术，原来要单方向转三下的情况可以改成只反着转一下，减少烦琐操作。
~~
三旋：
基于双旋再加上非通用的180°旋转，让需要转两下的情况可以一键完成，不过对效率提升效果不大。
~~
折返：
长按移动键将方块移动到靠墙的位置，然后再轻按反方向往回移动，减少在场地两侧摆块所需的按方向键的次数。
~~
撞墙转：
长按移动键将方块移动到靠墙的位置，然后再朝远离墙的方向旋转，可以让方块直接到达竖着离墙一格的位置，减少按键次数。

